import express from "express";
import dotenv from "dotenv";
import { existsSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { createServer as createViteServer } from "vite";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const root = resolve(__dirname, "..");
const isProduction = process.env.NODE_ENV === "production";
const port = Number(process.env.PORT || 5173);
const MODEL = process.env.ANTHROPIC_MODEL || "claude-haiku-4-5-20251001";
const ANTHROPIC_URL = "https://api.anthropic.com/v1/messages";
const ANTHROPIC_VERSION = "2023-06-01";

loadEnvironment();

const app = express();
app.use(express.json({ limit: "4mb" }));

app.get("/api/health", (_req, res) => {
  res.json({
    ok: true,
    provider: "Claude",
    configured: Boolean(process.env.ANTHROPIC_API_KEY),
    model: MODEL,
  });
});

app.post("/api/chat", async (req, res) => {
  try {
    const payload = normalizeChatPayload(req.body);
    const citations = buildCitations(payload.question, payload.selectedSources);
    const system = buildNotebookSystemPrompt(payload, citations);
    const messages = [
      ...payload.history.slice(-8),
      { role: "user", content: payload.question },
    ];

    const response = await callClaude({
      system,
      messages,
      maxTokens: payload.answerStyle === "Detailed" ? 1800 : 1300,
    });

    res.json({
      content: response.text,
      citations,
      provider: "Claude",
      model: MODEL,
      usage: response.usage,
    });
  } catch (error) {
    sendApiError(res, error);
  }
});

app.post("/api/artifact", async (req, res) => {
  try {
    const payload = normalizeArtifactPayload(req.body);
    const system = buildArtifactSystemPrompt(payload);
    const response = await callClaude({
      system,
      messages: [
        {
          role: "user",
          content: `Create a ${payload.kind} artifact for this notebook using the selected sources.`,
        },
      ],
      maxTokens: payload.kind === "report" ? 2200 : 1600,
    });

    res.json({
      title: payload.title,
      subtitle:
        payload.selectedSources.length > 0
          ? `Generated by Claude from ${payload.selectedSources.length} selected source${payload.selectedSources.length === 1 ? "" : "s"}`
          : "Generated by Claude without selected sources",
      body: response.text,
      provider: "Claude",
      model: MODEL,
      usage: response.usage,
    });
  } catch (error) {
    sendApiError(res, error);
  }
});

app.post("/api/discover", async (req, res) => {
  try {
    const topic = String(req.body?.topic || "").trim().slice(0, 160);
    if (!topic) {
      return res.status(400).json({ error: "Topic is required." });
    }

    const response = await callClaude({
      system:
        "You generate concise research starter sources for a NotebookLM-style workspace. Return tool input only.",
      messages: [
        {
          role: "user",
          content: `Create three useful source documents for researching: ${topic}`,
        },
      ],
      maxTokens: 1800,
      tools: [
        {
          name: "return_sources",
          description: "Return generated research starter sources.",
          input_schema: {
            type: "object",
            additionalProperties: false,
            required: ["sources"],
            properties: {
              sources: {
                type: "array",
                minItems: 3,
                maxItems: 3,
                items: {
                  type: "object",
                  additionalProperties: false,
                  required: ["title", "kind", "body"],
                  properties: {
                    title: { type: "string" },
                    kind: {
                      type: "string",
                      enum: ["doc", "url", "text", "slide", "ebook"],
                    },
                    body: { type: "string" },
                  },
                },
              },
            },
          },
        },
      ],
      toolChoice: { type: "tool", name: "return_sources" },
    });

    res.json({
      sources: sanitizeDiscoveredSources(response.toolInput?.sources),
      provider: "Claude",
      model: MODEL,
      usage: response.usage,
    });
  } catch (error) {
    sendApiError(res, error);
  }
});

