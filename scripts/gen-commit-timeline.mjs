#!/usr/bin/env node
/**
 * gen-commit-timeline.mjs
 * Reads the full git history and injects it into the "Build Journey" slide of
 * presentation/index.html (between the COMMITS_START / COMMITS_END markers).
 *
 * Run manually:        node scripts/gen-commit-timeline.mjs
 * Runs automatically:  via the .git/hooks/post-commit hook (every commit).
 *
 * The deck reads the resulting `const COMMITS = [...]` array and renders the
 * timeline client-side, so new commits show up after a browser refresh.
 */
import { execFileSync } from "node:child_process";
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const DECK = join(ROOT, "presentation", "index.html");
const US = ""; // unit separator — safe field delimiter

function git(args) {
  return execFileSync("git", args, { cwd: ROOT, encoding: "utf8", maxBuffer: 64 * 1024 * 1024 });
}

// One pass: commit headers (oldest→newest) interleaved with --shortstat lines.
const raw = git([
  "log",
  "--reverse",
  "--shortstat",
  "--no-merges",
  "--date=format:%Y-%m-%dT%H:%M",
  `--pretty=format:%h${US}%ad${US}%s`,
]);

const commits = [];
let cur = null;
for (const line of raw.split("\n")) {
  if (line.includes(US)) {
    if (cur) commits.push(cur);
    const [h, ad, s] = line.split(US);
    const [day, time] = (ad || "").split("T");
    cur = { h, day, time: time || "", s: (s || "").trim(), ins: 0, del: 0 };
  } else if (/files? changed/.test(line) && cur) {
    const mi = line.match(/(\d+) insertion/);
    const md = line.match(/(\d+) deletion/);
    if (mi) cur.ins = +mi[1];
    if (md) cur.del = +md[1];
  }
}
if (cur) commits.push(cur);

if (!commits.length) {
  console.error("gen-commit-timeline: no commits found, leaving deck untouched.");
  process.exit(0);
}

// Compact, HTML-safe JS literal (escape < so an accidental </script> can't break parsing).
const literal =
  "const COMMITS = " + JSON.stringify(commits).replace(/</g, "\\u003c") + ";";

const START = "/* COMMITS_START — auto-generated by scripts/gen-commit-timeline.mjs; do not edit by hand */";
const END = "/* COMMITS_END */";
const block = `${START}\n${literal}\n${END}`;

let html = readFileSync(DECK, "utf8");
const re = new RegExp(
  START.replace(/[.*+?^${}()|[\]\\]/g, "\\$&") + "[\\s\\S]*?" + END.replace(/[.*+?^${}()|[\]\\]/g, "\\$&"),
);
if (!re.test(html)) {
  console.error("gen-commit-timeline: markers not found in deck; aborting.");
  process.exit(1);
}
html = html.replace(re, block);
writeFileSync(DECK, html);

const days = new Set(commits.map((c) => c.day)).size;
console.log(`gen-commit-timeline: wrote ${commits.length} commits across ${days} day(s) into presentation/index.html`);
